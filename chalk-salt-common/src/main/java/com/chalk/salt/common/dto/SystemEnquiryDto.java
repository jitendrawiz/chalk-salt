package com.chalk.salt.common.dto;

/**
 * The Class SystemEnquiryDto.
 */
public class SystemEnquiryDto {
	/** The email. */
	private String email;

	/** The enquiry message. */
	private String enquiryMessage;

	/** The enquiry subject. */
	private String enquirySubject;

	/** The enquiry name. */
	private String enquiryName;

	/**
	 * Gets the email.
	 *
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * Sets the email.
	 *
	 * @param email the new email
	 */
	public void setEmail(String email) {
		this.email = email;
	}

	/**
	 * Gets the enquiry message.
	 *
	 * @return the enquiry message
	 */
	public String getEnquiryMessage() {
		return enquiryMessage;
	}

	/**
	 * Sets the enquiry message.
	 *
	 * @param enquiryMessage the new enquiry message
	 */
	public void setEnquiryMessage(String enquiryMessage) {
		this.enquiryMessage = enquiryMessage;
	}

	/**
	 * Gets the enquiry subject.
	 *
	 * @return the enquiry subject
	 */
	public String getEnquirySubject() {
		return enquirySubject;
	}

	/**
	 * Sets the enquiry subject.
	 *
	 * @param enquirySubject the new enquiry subject
	 */
	public void setEnquirySubject(String enquirySubject) {
		this.enquirySubject = enquirySubject;
	}

	/**
	 * Gets the enquiry name.
	 *
	 * @return the enquiry name
	 */
	public String getEnquiryName() {
		return enquiryName;
	}

	/**
	 * Sets the enquiry name.
	 *
	 * @param enquiryName the new enquiry name
	 */
	public void setEnquiryName(String enquiryName) {
		this.enquiryName = enquiryName;
	}
	
}
